#  static array, dynamic array , hash, queue, dequeue, stack, linked list

# static array (takes up memory addresses next to each other)
# reading O(1)
# search O(n) if not sorted
# insertion O(n) whole array must be moved in memeory
# deletion O(n) whole array must be moved if for instance the first element is deleted


# dynamic array
# same but insertions faster ~ O(1)


# linked lists (each elements stores the memory address of the next element)
# reading O(n)
# insertion O(1)
# deletion O(1) (element removed and previoues element has to point to the next one)

